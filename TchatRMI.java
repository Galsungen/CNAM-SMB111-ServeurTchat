    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Evenement quand on clic sur connecter
        Message message = new Message(jTextField1.getText(), " s'est connecté sur le serveur.");
        try {
            chat = (TchatInterface) Naming.lookup("rmi://"+jTextField3.getText()+":1099/MyTchat");
            chat.envoyerMessage(message);
            jTextField1.setEditable(false);
            jTextField3.setEditable(false);
            jButton2.setEnabled(false);
            jButton1.setEnabled(true);
            //System.out.println (jTextField1.getText()+" s'est connecté sur le serveur.");
            jTextArea3.setText(jTextArea3.getText()+message.toString());
            initOk = true;
        } catch (NotBoundException ex) {
            Logger.getLogger(TchatRMI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (MalformedURLException ex) {
            Logger.getLogger(TchatRMI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(TchatRMI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

