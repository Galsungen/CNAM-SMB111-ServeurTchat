    public void majFen(ArrayList<Message> message){
        if (message.size() > 0){
            for(Message tmp:message){
                //System.out.println (tmp);
                jTextArea3.setText(jTextArea3.getText()+tmp.toString());
            }
            try {
                chat.nettoyerMessage(this.getPseudo());
            } catch (RemoteException ex) {
                Logger.getLogger(TchatRMI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        this.repaint();
    }

    public String getPseudo(){
        return jTextField1.getText();
    }

